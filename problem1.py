# This function takes two inputs from a user and prints whether they are equal or not

def check_equality():
    # Get the first input from the user
    input1 = input("Enter the first value: ")
    
    # Get the second input from the user
    input2 = input("Enter the second value: ")
    
    # Check if the two inputs are equal
    if input1 == input2:
        print("The two inputs are equal.")
    else:
        print("The two inputs are not equal.")
